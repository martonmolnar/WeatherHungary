import UIKit

// Color definitions
extension UIColor {
    
    // NaviController bg color
    public class var middleMagenta: UIColor {
        return UIColor.init(red: 142/255, green: 6/255, blue: 73/255, alpha: 1)
    }
    // No Details text color
    public class var darkMagenta: UIColor {
        return UIColor.init(red: 93/255, green: 0/255, blue: 46/255, alpha: 1)
    }
    // White bg color
    public class var customWhite: UIColor {
        return UIColor.init(red: 237/255, green: 237/255, blue: 237/255, alpha: 1)
    }
   
    // Text
    public class var grayText: UIColor {
        return UIColor.init(red: 66/255, green: 64/255, blue: 79/255, alpha: 1)
    }
    
    
}
